// Map Category Name to Component helper
export const getCategoryIcon = (categoryName) => {
  const name = categoryName.toLowerCase();
  switch (name) {
    case 'food':
    case 'dining':
      return FoodIcon;
    case 'travel':
    case 'transport':
      return TravelIcon;
    case 'rent':
    case 'housing':
      return RentIcon;
    case 'utilities':
    case 'bills':
      return UtilitiesIcon;
    case 'entertainment':
    case 'leisure':
      return EntertainmentIcon;
    case 'shopping':
      return ShoppingIcon;
    case 'salary':
      return SalaryIcon;
    case 'freelance':
      return FreelanceIcon;
    case 'investments':
    case 'investment':
      return InvestmentIcon;
    default:
      return OthersIcon;
  }
};

